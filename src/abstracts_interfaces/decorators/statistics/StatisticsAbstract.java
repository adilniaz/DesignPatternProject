package abstracts_interfaces.decorators.statistics;

public abstract class StatisticsAbstract {
	public int health;
	public int speed;
	public int agility;
	public int strength;
	
	
	public int getHealth(){
		return health;
	}
	public int getSpeed(){
		return speed;
	}	
	public int getAgility(){
		return agility;
	}
	public int getStrength(){
		return strength;
	}
}
